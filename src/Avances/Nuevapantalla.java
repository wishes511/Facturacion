    private void nuevaspantalla() {
        pantalla p = new pantalla();
        p.setNombre(JtNombre.getText());
        p.setCorte((JtDetalle.getValueAt(0, 1).toString().equals("*")) ? "1" : "0");
        p.setPrecorte((JtDetalle.getValueAt(1, 1).toString().equals("*")) ? "1" : "0");
        p.setPespunte((JtDetalle.getValueAt(2, 1).toString().equals("*")) ? "1" : "0");
        p.setDeshebrado((JtDetalle.getValueAt(3, 1).toString().equals("*")) ? "1" : "0");
        p.setOjillado((JtDetalle.getValueAt(4, 1).toString().equals("*")) ? "1" : "0");
        p.setPreacabado((JtDetalle.getValueAt(5, 1).toString().equals("*")) ? "1" : "0");
        p.setInspeccion((JtDetalle.getValueAt(6, 1).toString().equals("*")) ? "1" : "0");
        p.setMontado((JtDetalle.getValueAt(7, 1).toString().equals("*")) ? "1" : "0");
        p.setMontado2((JtDetalle.getValueAt(8, 1).toString().equals("*")) ? "2" : "0");
        p.setPt((JtDetalle.getValueAt(9, 1).toString().equals("*")) ? "1" : "0");
        daoAvances a = new daoAvances();
        p.setPantalla(a.getmaxpantalla(c));
        if(a.nuevaspantallas(c, p)){
            JOptionPane.showMessageDialog(null, "Completo");
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Error al agregar nueva pantalla");
        }
        
    }

