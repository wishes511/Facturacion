    private void EjecutarPremovimiento() {
        String estilo = JtEstilo.getText();
        String almacen = JtAlmacen.getText();
        String cantidad = JtCantidad.getText();
        String precio = JtPrecio.getText();
        String tipo = JtTipo.getText().toLowerCase();
        if (estilo.equals("") || almacen.equals("") || cantidad.equals("") || precio.equals("") || tipo.equals("") || arrcomb.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Introducir correctamente los datos, intentarlo de nuevo");
            JtTipo.requestFocus();
        } else {
            if (!verificaint(estilo) && !verificaint(almacen) && !verificaint(cantidad) && !verificafloat(precio)
                    && (!tipo.equals("p") || !tipo.equals("s"))) {
                JOptionPane.showMessageDialog(null, "Introducir correctamente los datos, intentarlo de nuevo");
                JtEstilo.requestFocus();
            } else {
                if (verificadatostabla(Integer.parseInt(cantidad))) {
                    //ingresar lo faltante al objeto
                    int rowcomb = JlCombinacion.getSelectedIndex();
                    int rowcor = JlCorrida.getSelectedIndex();
                    String strcombi = arrcomb.get(rowcomb).getMaterial() + " - " + arrcomb.get(rowcomb).getColor();
                    prod.setCantidad(Integer.parseInt(cantidad));
                    prod.setPrecio(Float.parseFloat(precio));
                    prod.setTipo(tipo.toUpperCase());
                    prod.setDesccombinacion(strcombi);
                    prod.setCordesc(arrcorrida.get(rowcor).getDescripcion());
                    prod.setMarca(marca);
                    //aÃ±adir al kardex
                    cerrarbd();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Introducir correctamente los datos, intentarlo de nuevo");
                }
            }

        }
    }

    private boolean verificadatostabla(int cantidad) {// validacion de datos de tabla
        String resp = "";
        boolean r = false;
        boolean inicio = true;
        int punto = 1;
        int cant = 0;
        for (int i = 0; i < 12; i++) {// numero maximo de celdas
//            if (JtPares.getValueAt(0, i).equals("") || JtPares.getValueAt(0, i).equals("0")) {// verifica si es cero y saltarse la celda
            if (JtPares.getValueAt(0, i).equals("") || JtPares.getValueAt(0, i).equals("0")) {// verifica si es cero y saltarse la celda    
                setpuntos(punto, 0);
            } else {// distinta a 0 o vacio
                int celda = Integer.parseInt(JtPares.getValueAt(0, i).toString());// valor de la celda
                int p = Integer.parseInt(prod.getArr().get(i));// nvalor de existencia
//                System.out.println(JtPares.getRowCount() + " / " + JtPares.getSelectedRow());
                if (p >= celda && verificaint(String.valueOf(celda))) {//verifica punto x punto que exista y que no sea menos de cero
                    if (inicio) {
                        resp = " punto" + punto + "=punto" + punto + "-" + celda;
                        setpuntos(punto, celda);
                        inicio = false;
                    } else {
                        resp += ", punto" + punto + "=punto" + punto + "-" + celda;
                        setpuntos(punto, celda);
                    }

                    r = true;
                    cant += celda;
                } else {//cantidad de la celda mayor que la existencia
                    for (int j = 0; j < prod.getArr().size(); j++) {
                        JtPares.setValueAt("0", 0, j);
                    }
                    JOptionPane.showMessageDialog(null, "Alguna cantidad de pares esta mal, verificalo de nuevo");
                    resp = "";
                    break;
                }
            }
            punto++;
        }
        if (cantidad != cant) {
            r = false;
        }
//        System.out.println(resp + " - " + cantidad + " -| " + cant);
        return r;
    }

