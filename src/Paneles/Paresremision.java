    public final void generatabla() {// Genera la tabla desde cero con cada actualizacion
        DefaultTableModel model = new DefaultTableModel();
        daoproductos d = new daoproductos();
        //En el titulo genera los puntos
        //Datos iniciales para carga de datos y tabla
        float pi = arrcorrida.get(JlCorrida.getSelectedIndex()).getPi();// punto inicial
        float pf = (float) (arrcorrida.get(JlCorrida.getSelectedIndex()).getPf() + 0.5);// punto final
        int corrida = arrcorrida.get(JlCorrida.getSelectedIndex()).getCorrida();
        int comb = arrcomb.get(JlCombinacion.getSelectedIndex()).getCombinacion();
        int estilo = Integer.parseInt(JtEstilo.getText());
        int almacen = Integer.parseInt(JtAlmacen.getText());
        String bd = (JtTipo.getText().toUpperCase().equals("P")) ? "ExistenciasPedidos" : "ExistenciasStock";//

        prod = d.getProducto(rcpt, estilo, corrida, comb, almacen, bd);
        marca=(prod.getMarca().equals("1")) ? "O" : "T";
        JlMarcas.setText(marca);
        if (prod.getProducto() != 0) {
            model.setNumRows(1);
            JtPares.setRequestFocusEnabled(true);
            int i = 0;
            while (pi <= pf) {
                model.addColumn(pi + "\t\t        : " + prod.getArr().get(i));
                pi += 0.5;
                i++;
            }
            JtPares.setDragEnabled(false);
            JtPares.setModel(model);
            for (int j = 0; j < prod.getArr().size(); j++) {
                JtPares.setValueAt("", 0, j);
            }
        } else {
            JtTipo.requestFocus();
        }

    }

