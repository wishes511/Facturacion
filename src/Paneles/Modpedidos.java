    private void JbIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbIniciarActionPerformed
        daofactura df = new daofactura();
        String pedido = JtPedido.getText().toUpperCase();
        int f1 = Integer.parseInt(JtFac.getText());
        int f2 = Integer.parseInt(JtFacf.getText());
        if (df.actualizapedidos(rcpt, cpt, f1, f2, pedido)) {
            JOptionPane.showMessageDialog(null, "Proceso completo!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error al procesar datos, intentelo de nuevo");
            JtFac.requestFocus();
        }


    }//GEN-LAST:event_JbIniciarActionPerformed

