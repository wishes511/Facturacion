    private void setagente() {
        String id = JtId.getText();
        String nombre = JtNombre.getText().toUpperCase();
        Dao_Agente da = new Dao_Agente();
        if (nombre.isEmpty() && id.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Alguno de los datos esta vacio, intentalo de nuevo");
            JtId.requestFocus();
        } else {
            //Verifica la clave del agente
            if (!checkagente(da, Integer.parseInt(id))) {
                Agentes a = new Agentes();
                a.setIdagente(Integer.parseInt(id));
                a.setNombre(nombre);
                //En este hay duda ya que no se que es y para que sirve, pero por default:3
                a.setIdcanal(3);
                if (da.newagente(cob, a)) {
                    da.newagente(cobB, a);
                    JOptionPane.showMessageDialog(null, "Completo");
                    vaciarcampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al dar de alta, verfique los campos");

                }
                JtId.requestFocus();
            }
        }
    }

    /**
     * Funcion para verificar si existe o no la clave del agente
     *
     * @param da
     * @param id
     * @return
     */
    private boolean checkagente(Dao_Agente da, int id) {
        boolean r = da.getagente_Id(cob, id);
        if (r) {
            JOptionPane.showMessageDialog(null, "La clave del agente ya existe!, introduzca una distinta");
            JtId.requestFocus();
            JtId.setText("");
        }
        return r;
    }

