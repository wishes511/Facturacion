    private void getprov() {
        DefaultListModel<String> model = new DefaultListModel<>();
        daocfdi d = new daocfdi();
        arrclave = d.getclaveprod(litecfdi, JtCodigo.getText());
        for (Claveprov arrclave1 : arrclave) {
            model.addElement(arrclave1.getClaveprod() + "-" + arrclave1.getDescripcion());
        }
        JlSat.setModel(model);
        if (!arrclave.isEmpty()) {
            JlSat.requestFocus();
            JlSat.setSelectedIndex(0);
        }
    }

    /**
     * Formatear para que no tome en cuenta los espacios
     *
     * @param mat
     * @return
     */
    private String getmatformat(String mat) {
        String resp = "";
        for (int i = 0; i < mat.length(); i++) {
            String caracter = mat.charAt(i) + "";
            if (!caracter.equals(" ")) {
                resp += caracter;
            }
        }
        return resp;
    }

    private boolean verificafloat(String cad) {
        boolean resp = false;
        String patt = "[0-9]+||[0-9]+.[0-9]+";
        Pattern pat = Pattern.compile(patt);
        Matcher match = pat.matcher(cad);
        if (match.matches()) {
            resp = true;
        }
        return resp;
    }

