    private void JmImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JmImprimirActionPerformed
        try {
            Map parametros = new HashMap();
            int ped=arr.get(JtDetalle.getSelectedRow()).getId_pedimento();
            System.out.println(ped);
            parametros.put("id", ped);
            JasperReport jasper = (JasperReport) JRLoader.loadObject(getClass().getResource("/Reportestpu/Pedimento.jasper"));
            JasperPrint print = JasperFillManager.fillReport(jasper, parametros, c.getCpttpu());
            JasperViewer ver = new JasperViewer(print, false); //despliegue de reporte
            ver.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            ver.setTitle("Pedimento " + arr.get(JtDetalle.getSelectedRow()).getReferencia());
            ver.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(Pedimento1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_JmImprimirActionPerformed

    private void settabla() {
        daopedimentos s = new daopedimentos();
        arr = s.getallpedimento(c.getCpttpu(), JtCliente.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Pedimento");
        model.addColumn("Total");
        model.addColumn("Cantidad");
        model.addColumn("Proveedor");
        model.setNumRows(arr.size());
        for (int i = 0; i < arr.size(); i++) {
            model.setValueAt(arr.get(i).getReferencia(), i, 0);
            model.setValueAt(arr.get(i).getTotal(), i, 1);
            model.setValueAt(arr.get(i).getTcantidad(), i, 2);
            model.setValueAt(arr.get(i).getNprov(), i, 3);
        }
        JtDetalle.setModel(model);
    }

