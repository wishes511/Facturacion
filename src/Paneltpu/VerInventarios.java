    private void jLabel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MousePressed
        int row = JcFecha.getSelectedIndex();
        String mes = arr.get(row).getMes() + "";
        String years = arr.get(row).getYears() + "";
        setreport(mes, years);
    }//GEN-LAST:event_jLabel1MousePressed

    private void setreport(String mes, String years) {
        try {
            Map parametros = new HashMap();
//            Agregar parametros al reporte
            parametros.put("mes", mes);
            parametros.put("years", years);
            JasperReport jasper = (JasperReport) JRLoader.loadObject(getClass().getResource("/Reportestpu/Pedimento_inventarioPasado.jasper"));
            JasperPrint print = JasperFillManager.fillReport(jasper, parametros, cpt);
            JasperViewer ver = new JasperViewer(print, false); //despliegue de reporte
            this.dispose();
            ver.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            ver.setTitle("Saldos vencidos");
            ver.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(fac1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public boolean setcombo() {
        daoInventarios di = new daoInventarios();
        arr = di.getinventarios(cpt, 0, 0);
        return arr.isEmpty();
    }

    public void llenacombo() {
        if (!arr.isEmpty()) {
            DefaultComboBoxModel per = new DefaultComboBoxModel();
            for (Inventario arr1 : arr) {
                per.addElement(arr1.getMes() + " - " + arr1.getYears());
            }
            JcFecha.setModel(per);
        }
    }

    /**
     * Formatear para que no tome en cuenta los espacios
     *
     * @param mat
     * @return
     */
    private String getmatformat(String mat) {
        String resp = "";
        for (int i = 0; i < mat.length(); i++) {
            String caracter = mat.charAt(i) + "";
            if (!caracter.equals(" ")) {
                resp += caracter;
            }
        }
        return resp;
    }

    private boolean verificafloat(String cad) {
        boolean resp = false;
        String patt = "[0-9]+||[0-9]+.[0-9]+";
        Pattern pat = Pattern.compile(patt);
        Matcher match = pat.matcher(cad);
        if (match.matches()) {
            resp = true;
        }
        return resp;
    }

