    private void jLabel2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MousePressed
        Kardexrcpt ka = new Kardexrcpt();
        Calendar fecha = Calendar.getInstance();
        int rowcliente = JcCliente.getSelectedIndex();
        ///////Formateo de fechas y hrs
        int año = fecha.get(Calendar.YEAR);
        int mes = fecha.get(Calendar.MONTH) + 1;
        int dia = fecha.get(Calendar.DAY_OF_MONTH);
        int hora = fecha.get(Calendar.HOUR_OF_DAY);
        int minuto = fecha.get(Calendar.MINUTE);
        String month = (mes < 10) ? "0" + mes : "" + mes;
        String day = (dia < 10) ? "0" + dia : "" + dia;
        String fechas = año +"-"+ month +"-"+ day + "T" + formatohr(hora, minuto) + ":00";
        //////// Fin de formateo de fechas
        daokardexrcpt dk = new daokardexrcpt();
        ka.setFolio(dk.maxkardex(rcpt) + 1);
        ka.setPedido((dk.maxpedido(rcpt)+1)+ " " + arrprod.get(0).getMarca());
        ka.setCuenta(JtCuenta.getText());
        ka.setSubcuenta(JtSubcuenta.getText());
        ka.setFecha(fechas);
        ka.setRegistro(fechas);
        ka.setPrv(arrclientes.get(rowcliente).getCvecliente());
        ka.setVale(1);
        ka.setDocref("1");
        ka.setSerie(serie);
        ka.setAgente(arragentes.get(JcAgente.getSelectedIndex()).getIdagente());
        ka.setMarca(marca);
        ka.setArr(arrprod);
        daokardexrcpt dkardex = new daokardexrcpt();
        
        if(dkardex.nuevokardex(rcpt, ka)){
            JOptionPane.showMessageDialog(null, "Completo");
            vaciado();
        }else
            JOptionPane.showMessageDialog(null, "Ocurrio algo, vuelve a intentarlo o llama a sistemas c:");
        
        
    }//GEN-LAST:event_jLabel2MousePressed

