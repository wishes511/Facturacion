    private String formatohr(int hora, int minuto) {
        String horas = "";
        if (hora > 9) {
            horas = hora + ":";
        }
        if (hora < 10) {
            horas = "0" + hora + ":";
        }
        if (minuto < 10) {
            horas = horas + "0" + minuto;
        }
        if (minuto > 9) {
            horas += minuto;
        }
//                System.out.println(horas);
        horas = (horas.length() == 5) ? horas : "0" + horas;
        return horas;
    }

