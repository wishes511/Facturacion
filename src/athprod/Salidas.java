    public final void generatabla() {

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tipo");//0
        model.addColumn("Almacen");//1
        model.addColumn("Estilo");//2
        model.addColumn("Combinacion");//3
        model.addColumn("Corrida");//4
        model.addColumn("Cantidad");//5
        model.addColumn("Precio");//6
        if (!arrprod.isEmpty()) {// rellena de datos de acuerdo a las lineas que se capturen
            model.setRowCount(arrprod.size());
            for (int i = 0; i < arrprod.size(); i++) {
                model.setValueAt(arrprod.get(i).getTipo(), i, 0);
                model.setValueAt(arrprod.get(i).getAlmacen(), i, 1);
                model.setValueAt(arrprod.get(i).getEstilo(), i, 2);
                model.setValueAt(arrprod.get(i).getDesccombinacion(), i, 3);
                model.setValueAt(arrprod.get(i).getCordesc(), i, 4);
                model.setValueAt(arrprod.get(i).getCantidad(), i, 5);
                model.setValueAt(arrprod.get(i).getPrecio(), i, 6);
            }
        }
        JtMovimientos.setModel(model);
    }

